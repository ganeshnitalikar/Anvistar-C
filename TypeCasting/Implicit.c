// IMPLICIT TYPE CASTING 
// IT IS A TECHNIQUE THROUGH WHICH WE CAN CONVERT ONE TYPE OF VALUE INTO ANOTHER, IMPLICITLY BY COMPILER(AUTOMATICALLY DONE BY COMPILER)
// EXAMPLE

#include <stdio.h>
int main()
{
    int x = 65;
    char y = 'A';

    printf("%c",x); //A

    printf("\n%d",y); //65

    return 0;

}